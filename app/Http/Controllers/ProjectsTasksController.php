     */
    public function update( Project $project, Task $task)
    {
        $this->authorize('userProject',$project);
        request()->validate(['body' => 'required']);
        
        $task ->update([
            'body' => request('body'),
        ]);
        
        request('completed') ? $task->complete() : $task->incomplete();


        return redirect($project->path());
    }

